<h1>About CMU Course Find</h1>

<p>
  CMU Course Find is a 15-112 term project by Justin Chu (me) during the Fall
  2015 semester at Carnegie Mellon University. It has been updated over the
  years.
</p>
<p>
  I had this idea one day when I was wandering in Wean Hall. I saw students
  having class and hoped to find out what class they were having. I could just
  walk in and see what was going on, but it would be much better if I knew
  something about the class.
</p>
<p>
  CMU Course Find makes it easier for you to figure out what’s going on in a
  class room. You can also see
  <b>what’s happening</b>
  at a particular time, so you can
  <b>go and discover</b>
  courses that interest you. This web app is also great for finding the room for
  a class, or just browsing the catalog to see what’s interesting.
</p>
<p>
  To find a course, you may search for the name of a course, name of
  instructors, name of a building, number of a course, or a room number.
</p>
<p>
  If you have any suggestion or if you would like to contribute to this project,
  please don’t hesitate to submit a feedback!
</p>
